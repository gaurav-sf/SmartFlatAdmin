package com.grs.product.smartflatAdmin.models;

public class ContactDetails {
	
	private String mContactName;
	private String mContactNumber;
	private String mContactEmailId;
	private String mContactOccupation;
	
	
	public String getmContactName() {
		return mContactName;
	}
	
	public void setmContactName(String mContactName) {
		this.mContactName = mContactName;
	}
	
	public String getmContactNumber() {
		return mContactNumber;
	}
	
	public void setmContactNumber(String mContactNumber) {
		this.mContactNumber = mContactNumber;
	}
	
	public String getmContactEmailId() {
		return mContactEmailId;
	}
	
	public void setmContactEmailId(String mContactEmailId) {
		this.mContactEmailId = mContactEmailId;
	}
	
	public String getmContactOccupation() {
		return mContactOccupation;
	}
	
	public void setmContactOccupation(String mContactOccupation) {
		this.mContactOccupation = mContactOccupation;
	}

}

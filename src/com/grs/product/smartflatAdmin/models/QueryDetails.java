package com.grs.product.smartflatAdmin.models;

public class QueryDetails {
	
	private String mQueryNumber;	
	private String mQueryDateTime;	
	private String mQueryStatus;	
	private String mQueryDetails;
	private String mQueryRaisedBy;
	
	public String getmQueryNumber() {
		return mQueryNumber;
	}
	
	public void setmQueryNumber(String mQueryNumber) {
		this.mQueryNumber = mQueryNumber;
	}
	
	public String getmQueryDateTime() {
		return mQueryDateTime;
	}
	
	public void setmQueryDateTime(String mQueryDateTime) {
		this.mQueryDateTime = mQueryDateTime;
	}
	
	public String getmQueryStatus() {
		return mQueryStatus;
	}
	
	public void setmQueryStatus(String mQueryStatus) {
		this.mQueryStatus = mQueryStatus;
	}
	
	public String getmQueryDetails() {
		return mQueryDetails;
	}
	
	public void setmQueryDetails(String mQueryDetails) {
		this.mQueryDetails = mQueryDetails;
	}

	public String getmQueryRaisedBy() {
		return mQueryRaisedBy;
	}

	public void setmQueryRaisedBy(String mQueryRaisedBy) {
		this.mQueryRaisedBy = mQueryRaisedBy;
	}

}
